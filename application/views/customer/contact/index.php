<script>
    var validator;

    var FormValidation = function() {
        // Validation config
        var _componentValidation = function() {
            if (!$().validate) {
                console.warn('Warning - validate.min.js is not loaded.');
                return;
            }

            // Initialize
            validator = $('.form-validate-jquery').validate({
                ignore: 'input[type=hidden], .select2-search__field', // ignore hidden fields
                errorClass: 'validation-invalid-label',
                successClass: 'validation-valid-label',
                validClass: 'validation-valid-label',
                highlight: function(element, errorClass) {
                    $(element).removeClass(errorClass);
                },
                unhighlight: function(element, errorClass) {
                    $(element).removeClass(errorClass);
                },
                success: function(label) {
                    label.addClass('validation-valid-label').text('success.'); // remove to hide Success message
                },

                // Different components require proper error label placement
                errorPlacement: function(error, element) {

                    // Unstyled checkboxes, radios
                    if (element.parents().hasClass('form-check')) {
                        error.appendTo( element.parents('.form-check').parent() );
                    }

                    // Input with icons and Select2
                    else if (element.parents().hasClass('form-group-feedback') || element.hasClass('select2-hidden-accessible')) {
                        error.appendTo( element.parent() );
                    }

                    // Input group, styled file input
                    else if (element.parent().is('.uniform-uploader, .uniform-select') || element.parents().hasClass('input-group')) {
                        error.appendTo( element.parent().parent() );
                    }

                    // Other elements
                    else {
                        error.insertAfter(element);
                    }
                },
                rules: {
                    name: {
                        required: true
                    },
                    email: {
                        required: true
                    },
                    phone: {
                        required: true
                    },
                    message: {
                        required: true
                    },
                },
                messages: {
                    name: {
                        required: 'Please insert your name.'
                    },
                    email: {
                        required: 'Please insert your email.'
                    },
                    phone: {
                        required: 'Please insert your phone number.'
                    },
                    message: {
                        required: 'Please insert your message.'
                    },
                }
            });
        };

        return {
            init: function() {
                _componentValidation();
            }
        }
    }();

    document.addEventListener('DOMContentLoaded', function() {
        FormValidation.init();
    });

    function send_message(){
        var check = validator.checkForm();

        if (!check)
            validator.showErrors();
        else{
            $.ajax({
                url: base_url+'customer/contact/insert_Contact',
                type : 'POST',
                data : {
                    name: $('#name').val(),
                    phone: $('#phone').val(),
                    email: $('#email').val(),
                    content:$('#message').val(),
                },
                cache: false,
                success: function(result) {

                    new PNotify({
                        title: 'SUCCESS!',
                        text: 'We received your message and we will get back to you soon.',
                        icon: 'icon-checkmark3',
                        type: 'success'
                    });
                    location.reload();
                }
            });
        }
    }

</script>
